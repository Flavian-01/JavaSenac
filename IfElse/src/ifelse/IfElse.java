
package ifelse;


public class IfElse {

 
    public static void main(String[] args) {
        int idade = 85;
        
        if (idade <= 40)
        {
            System.out.println("Menor que 40");
        }
        else if (idade <= 60)
        {
            System.out.println("Menor que 60");
        }
        else if (idade <= 80)
        {
            System.out.println("Menor que 80");
        }
        else if (idade <= 100)
        {
            System.out.println("Menor que 100");
        
        
        }
    }
    
}
